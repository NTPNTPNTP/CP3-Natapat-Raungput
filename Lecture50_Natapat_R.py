def addNumber(num_1, num_2):
    print(num_1+num_2)

def deleteNumber(num_1, num_2):
    print(num_1-num_2)

def multiplyNumber(num_1, num_2):
    print(num_1*num_2)

def divideNumber(num_1, num_2):
    print(num_1/num_2)

addNumber(10, 2)
deleteNumber(10, 2)
multiplyNumber(10, 2)
divideNumber(10, 2)
